function getStoredLanguage() {
  return localStorage.getItem('tax_lang') || 'fa';
}

function getStoredTheme() {
  return localStorage.getItem('tax_theme') || 'dark';
}

function applyLanguage(language) {
  const dictionary = translations[language];
  document.documentElement.lang = language;
  document.documentElement.dir = dictionary.dir;
  document.body.classList.toggle('en', language === 'en');
  const langToggle = document.getElementById('langToggle');
  if (langToggle) langToggle.textContent = dictionary.langToggle;
  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) element.textContent = dictionary[key];
  });
  localStorage.setItem('tax_lang', language);
}

function applyTheme(theme) {
  document.body.classList.toggle('dark', theme === 'dark');
  localStorage.setItem('tax_theme', theme);
}

function formatNumber(number) {
  const language = getStoredLanguage();
  return new Intl.NumberFormat(language === 'fa' ? 'fa-IR' : 'en-US').format(number);
}

function calculateTax(income) {
  if (income <= 100000000) return { rate: 10, tax: income * 0.1 };
  if (income <= 300000000) return { rate: 15, tax: income * 0.15 };
  return { rate: 20, tax: income * 0.2 };
}

function generateNumericId(length) {
  let output = '';
  for (let index = 0; index < length; index += 1) {
    output += Math.floor(Math.random() * 10).toString();
  }
  return output;
}

function bindLanguageToggle() {
  const langToggle = document.getElementById('langToggle');
  if (!langToggle) return;
  langToggle.addEventListener('click', () => {
    const nextLanguage = getStoredLanguage() === 'fa' ? 'en' : 'fa';
    applyLanguage(nextLanguage);
  });
}

function bindThemeToggle() {
  const themeToggle = document.getElementById('themeToggle');
  if (!themeToggle) return;
  themeToggle.addEventListener('click', () => {
    const nextTheme = getStoredTheme() === 'dark' ? 'light' : 'dark';
    applyTheme(nextTheme);
  });
}

function bindCalculator() {
  const calculateBtn = document.getElementById('calculateBtn');
  if (!calculateBtn) return;
  calculateBtn.addEventListener('click', () => {
    const incomeInput = document.getElementById('incomeInput');
    const income = Number(incomeInput.value);
    const language = getStoredLanguage();
    if (!income || income <= 0) {
      alert(translations[language].invalidIncome);
      return;
    }
    const result = calculateTax(income);
    document.getElementById('incomeResult').textContent = `${formatNumber(income)} ${translations[language].toman}`;
    document.getElementById('rateResult').textContent = `${result.rate}%`;
    document.getElementById('taxResult').textContent = `${formatNumber(result.tax)} ${translations[language].toman}`;
    document.getElementById('resultBox').classList.remove('hidden');
  });
}

function bindReceiptIssuer() {
  const issueBtn = document.getElementById('issueReceiptBtn');
  if (!issueBtn) return;
  issueBtn.addEventListener('click', () => {
    const language = getStoredLanguage();
    const payerName = document.getElementById('payerName').value.trim();
    const amount = Number(document.getElementById('billAmount').value);
    if (!payerName || !amount || amount <= 0) {
      alert(translations[language].invalidReceipt);
      return;
    }
    document.getElementById('receiptNameValue').textContent = payerName;
    document.getElementById('receiptAmountValue').textContent = `${formatNumber(amount)} ${translations[language].toman}`;
    document.getElementById('receiptBillIdValue').textContent = generateNumericId(13);
    document.getElementById('receiptPayIdValue').textContent = generateNumericId(10);
    document.getElementById('receiptResult').classList.remove('hidden');
  });
}

applyTheme(getStoredTheme());
applyLanguage(getStoredLanguage());
bindLanguageToggle();
bindThemeToggle();
bindCalculator();
bindReceiptIssuer();
